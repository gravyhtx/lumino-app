/**
 * @enum {Record<string, ColorNameStrings>} RGB, HSL, LAB, LCH, and CMYK strings for CSS color names.
 * @readonly
 */
const STRINGS = Object.entries(VALUES).reduce((acc, [colorName, colorValues]) => {
	// RGB to string
	function rgbArrayToString(rgb: readonly [number, number, number]): string {
		return `rgb(${rgb[0]}, ${rgb[1]}, ${rgb[2]})`;
	}
	// HSL to string
	function hslArrayToString(hsl: readonly [number, number, number]): string {
		return `hsl(${hsl[0]}, ${hsl[1]}%, ${hsl[2]}%)`;
	}
  // LCH to string
  function lchArrayToString(lch: readonly [number, number, number]): string {
    return `lch(${lch[0]}, ${lch[1]}%, ${lch[2]})`;
  }
  // LAB to string
  function labArrayToString(lab: readonly [number, number, number]): string {
    return `lab(${lab[0]}, ${lab[1]}, ${lab[2]})`;
  }
  // CMYK to string
  function cmykArrayToString(cmyk: readonly [number, number, number, number]): string {
    return `cmyk(${cmyk[0]}%, ${cmyk[1]}%, ${cmyk[2]}%, ${cmyk[3]}%)`;
  }
  const hex = String(HEX_STRING[colorName]);
  const rgb = rgbArrayToString(colorValues.rgb);
  const hsl = hslArrayToString(colorValues.hsl);
  const lch = lchArrayToString(colorValues.lch);
  const lab = labArrayToString(colorValues.lab);
  const cmyk = cmykArrayToString(colorValues.cmyk);
  acc[colorName] = { hex, rgb, hsl, lch, lab, cmyk };
  return acc;
}, {} as Record<string, ColorNameStrings>);

